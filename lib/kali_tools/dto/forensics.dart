extension KaliToolsForensicsEnumExt on KaliToolsForensicsEnum {
  String get package {
    switch (this) {
      default:
        return _kaliToolsForensicsMap[this]!;
    }
  }
}

Future<ToolSelectionResult> selectKaliToolsForensicsToInstall() =>
    selectToolsToInstall<KaliToolsForensicsEnum>(
      groupName: 'kali-tools-forensics',
      values: KaliToolsForensicsEnum.values,
      packageNameGetter: (e) => e.package,
    );
